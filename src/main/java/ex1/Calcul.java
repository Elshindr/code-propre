package ex1;

/**
 * The type Calcul.
 */
public class Calcul {


	/**
	 * Get addition int.
	 *
	 * @param a the a
	 * @param b the b
	 * @return the int
	 */
	public int getAddition(int a, int b){
		return a+b;
	}


	/**
	 * Get soustration int.
	 *
	 * @param a the a
	 * @param b the b
	 * @return the int
	 */
	public int getSoustration(int a, int b){
		return a-b;
	}
}
